[StructLayout(LayoutKind.Explicit)]
public unsafe struct AtelCallTarget {
    [FieldOffset(0x0)] public nint init_func;
    [FieldOffset(0x4)] public nint exec_func;
    [FieldOffset(0x8)] public nint ret_float_func;
    [FieldOffset(0xC)] public nint ret_int_func;
}

public static unsafe class AtelCallTargets {
    static AtelCallTargets() {
        // Assert namespace lengths to avoid a dumb warning
        // and still make people do things properly
        Assert(Common.Length  == 616);
        Assert(Math.Length    == 30);
        Assert(SgEvent.Length == 71);
        Assert(ChEvent.Length == 145);
        Assert(Camera.Length  == 138);
        Assert(Battle.Length  == 296);
        Assert(Map.Length     == 108);
        Assert(Mount.Length   == 1);
        Assert(Movie.Length   == 16);
        Assert(Debug.Length   == 94);
        Assert(AbiMap.Length  == 1);
    }

    public static string get_fn_name(ushort id, bool include_namespace) {
        CallTarget ct = CallTarget.get(id);

        if (include_namespace) {
            AtelCallTargetNamespace nmsp = (AtelCallTargetNamespace)(id >> 0xC);
            return $"{nmsp.to_string()}::{ct.name}";
        }

        return ct.name;
    }

    public class CallTarget(string name, (string name, Type type)[] args, Type? return_type) {
        public string name = name;
        public (string name, Type type)[] args = args;
        public Type? return_type = return_type;

        public CallTarget(ushort id, int arg_count) : this($"{id:X4}", [], null) {
            args = new (string, Type)[arg_count];
            for (int i = 0; i < arg_count; i++) {
                args[i] = ($"p{i}", typeof(UnknownType));
            }

            AtelCallTargetNamespace nmsp = (AtelCallTargetNamespace)(id >> 0xC);
            AtelCallTarget* internal_ct = nmsp.get_internal() + (id & 0xFFF);
            if (internal_ct->ret_float_func != 0) return_type = typeof(float);
            else if (internal_ct->ret_int_func != 0) return_type = typeof(int);
            else return_type = typeof(UnknownType);
        }

        public static CallTarget get(ushort id) {
            AtelCallTargetNamespace nmsp = (AtelCallTargetNamespace)(id >> 0xC);
            int idx = id & 0xFFF;
            return nmsp.get()[idx];
        }
    }

    [StructLayout(LayoutKind.Explicit)]
    public struct UnknownType {
        [FieldOffset(0x0)] public int as_int;
        [FieldOffset(0x0)] public float as_float;
    }

